param(
    [string]$OutputDll = 'C:\ClashTests\cdb-surface-dump\ddraw.dll',
    [string]$LogDir = ''
)

$ErrorActionPreference = 'Stop'

function Get-FileSha256 {
    param([string]$Path)
    (Get-FileHash -Algorithm SHA256 -LiteralPath $Path).Hash
}

function Find-VcVars32 {
    $candidates = @(
        'C:\Program Files (x86)\Microsoft Visual Studio\2022\BuildTools\VC\Auxiliary\Build\vcvars32.bat',
        'C:\Program Files\Microsoft Visual Studio\2022\BuildTools\VC\Auxiliary\Build\vcvars32.bat',
        'C:\Program Files (x86)\Microsoft Visual Studio\2022\Community\VC\Auxiliary\Build\vcvars32.bat',
        'C:\Program Files\Microsoft Visual Studio\2022\Community\VC\Auxiliary\Build\vcvars32.bat'
    )
    foreach ($candidate in $candidates) {
        if (Test-Path -LiteralPath $candidate) {
            return (Get-Item -LiteralPath $candidate).FullName
        }
    }
    $found = Get-ChildItem 'C:\Program Files','C:\Program Files (x86)' -Recurse -Filter vcvars32.bat -ErrorAction SilentlyContinue |
        Select-Object -First 1
    if ($found) {
        return $found.FullName
    }
    throw 'Could not find vcvars32.bat. Install Visual Studio Build Tools with the C++ toolchain.'
}

$repoRoot = [System.IO.Path]::GetFullPath((Join-Path $PSScriptRoot '..\..'))
$source = Join-Path $repoRoot 'ddraw_surfdump_proxy\ddraw_surfdump_proxy.cpp'
if (-not (Test-Path -LiteralPath $source)) {
    throw "Source file was not found: $source"
}
$defFile = Join-Path $repoRoot 'ddraw_surfdump_proxy\ddraw_surfdump_proxy.def'
if (-not (Test-Path -LiteralPath $defFile)) {
    throw "DEF file was not found: $defFile"
}

$outputFull = [System.IO.Path]::GetFullPath($OutputDll)
$outputDir = Split-Path -Parent $outputFull
if (-not (Test-Path -LiteralPath $outputDir)) {
    New-Item -ItemType Directory -Path $outputDir -Force | Out-Null
}
if (-not $LogDir) {
    $LogDir = $outputDir
}
$LogDir = [System.IO.Path]::GetFullPath($LogDir)
if (-not (Test-Path -LiteralPath $LogDir)) {
    New-Item -ItemType Directory -Path $LogDir -Force | Out-Null
}

$manifestPath = Join-Path $outputDir 'ddraw_surfdump_proxy.build.json'
if (Test-Path -LiteralPath $outputFull) {
    $existingOk = $false
    if (Test-Path -LiteralPath $manifestPath) {
        try {
            $manifest = Get-Content -LiteralPath $manifestPath -Raw | ConvertFrom-Json
            $existingOk = $manifest.generated_by -eq 'clash-hd-surface-dump-proxy'
        }
        catch {
            $existingOk = $false
        }
    }
    if (-not $existingOk) {
        throw "Refusing to overwrite an existing ddraw.dll that was not generated by this build script: $outputFull"
    }
}

$vcvars = [string](Find-VcVars32)
$buildDir = Join-Path $LogDir 'ddraw-proxy-build'
New-Item -ItemType Directory -Path $buildDir -Force | Out-Null
$cmdPath = Join-Path $buildDir 'build-ddraw-surfdump-proxy.cmd'
$buildLog = Join-Path $buildDir 'build.log'
$objDir = Join-Path $buildDir 'obj'
New-Item -ItemType Directory -Path $objDir -Force | Out-Null

$objOut = Join-Path $objDir 'ddraw_surfdump_proxy.obj'
$cmd = @"
@echo off
setlocal
call "$vcvars" >nul
cl /nologo /EHsc /LD /W3 /DWIN32 /D_WINDOWS /Fo"$objOut" /Fe"$outputFull" "$source" /link /DEF:"$defFile" user32.lib gdi32.lib
exit /b %ERRORLEVEL%
"@
Set-Content -LiteralPath $cmdPath -Value $cmd -Encoding ASCII

Push-Location -LiteralPath $buildDir
try {
    & $env:ComSpec /d /c (Split-Path -Leaf $cmdPath) *> $buildLog
    $exitCode = $LASTEXITCODE
}
finally {
    Pop-Location
}
if ($exitCode -ne 0) {
    Get-Content -LiteralPath $buildLog -ErrorAction SilentlyContinue | Select-Object -Last 80
    throw "ddraw surface-dump proxy build failed with exit code $exitCode. See $buildLog"
}

$sourceSha = Get-FileSha256 -Path $source
$dllSha = Get-FileSha256 -Path $outputFull
$manifestObject = [pscustomobject]@{
    generated_by = 'clash-hd-surface-dump-proxy'
    source = $source
    source_sha256 = $sourceSha
    output = $outputFull
    output_sha256 = $dllSha
    vcvars32 = $vcvars
    build_log = $buildLog
    built_at = (Get-Date).ToString('s')
}
$manifestObject | ConvertTo-Json -Depth 4 | Set-Content -LiteralPath $manifestPath -Encoding ASCII

Write-Host "Built DirectDraw surface-dump proxy: $outputFull"
Write-Host "SHA-256: $dllSha"
Write-Host "Build log: $buildLog"
